package course_cod3r.modulo_07_desafio;

public class ClienteEndereco {

	String cep;
	String rua;
	String casa;
	String complemento;
	
	public ClienteEndereco() {
		
	}
	
	public ClienteEndereco(String cep, String rua, String casa, String complemento) {
		this.cep = cep;
		this.rua = rua;
		this.casa = casa;
		this.complemento = complemento;
	}
	
}
